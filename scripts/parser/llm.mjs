export function getLLM() {
  if (process.env.ANTHROPIC_API_KEY || process.env.ANTHROPIC_AUTH_TOKEN) {
    const client = new Anthropic();
    const model = process.env.LLM_MODEL || "claude-opus-4-8";
    const usage = { cacheRead: 0, cacheCreate: 0, input: 0, calls: 0 };
    return {
      name: `claude:${model}`,
      usage,
      async rewrite(term, raw) {
        const resp = await client.messages.create({
          model,
          max_tokens: 600,
          output_config: { effort: "low" },
          // системный блок стабилен между вызовами → кэшируем его (cache_control).
          // переменная часть (термин+определение) идёт в user, ПОСЛЕ точки кэша.
          system: [{ type: "text", text: SYSTEM, cache_control: { type: "ephemeral" } }],
          messages: [{
            role: "user",
            content: `Термин: ${term}\nИсходное определение (из источника, только как опора — НЕ копировать дословно):\n${raw}`,
          }],
        });
        const u = resp.usage || {};
        usage.cacheRead += u.cache_read_input_tokens || 0;
        usage.cacheCreate += u.cache_creation_input_tokens || 0;
        usage.input += u.input_tokens || 0;
        usage.calls += 1;
        const block = resp.content.find((b) => b.type === "text");
        return (block?.text || "").trim();
      },
    };
  }

  if (process.env.OPENAI_API_KEY) {
    const key = process.env.OPENAI_API_KEY;
    const model = process.env.LLM_MODEL || "gpt-4o-mini";
    return {
      name: `openai:${model}`,
      async rewrite(term, raw) {
        const res = await fetch("https://api.openai.com/v1/chat/completions", {
          method: "POST",
          headers: { Authorization: `Bearer ${key}`, "Content-Type": "application/json" },
          body: JSON.stringify({
            model,
            max_tokens: 400,
            messages: [
              { role: "system", content: SYSTEM },
              { role: "user", content: `Термин: ${term}\nИсходное определение (опора, не копировать):\n${raw}` },
            ],
          }),
        });
        const json = await res.json();
        if (!res.ok) throw new Error(JSON.stringify(json));
        return json.choices[0].message.content.trim();
      },
    };
  }

  return null; // ключей нет — режим без LLM (--no-llm)
}
