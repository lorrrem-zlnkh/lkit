function genericNormalize(text) {
  return String(text)
    .replaceAll("AI-сервис", "ИИ-сервис")
    .replaceAll("AI-инструмент", "ИИ-инструмент")
    .replaceAll("AI-платформа", "ИИ-платформа")
    .replaceAll("AI-поиск", "ИИ-поиск")
    .replaceAll("AI-помощник", "ИИ-помощник")
    .replaceAll("AI-ассистент", "ИИ-ассистент")
    .replaceAll("AI-моделей", "ИИ-моделей")
    .replaceAll("AI-моделям", "ИИ-моделям")
    .replaceAll("AI-модель", "ИИ-модель")
    .replaceAll("AI tools", "ИИ-инструменты")
    .replaceAll("storytelling", "сторителлинга")
    .replaceAll("storytelling-контента", "контента для сторителлинга")
    .replaceAll("motion-контента", "моушн-контента")
    .replaceAll("talking-head", "говорящих")
    .replaceAll("text-to-video", "генерации видео по тексту")
    .replaceAll("idea-to-video", "от идеи к видео")
    .replaceAll("no-code", "no-code")
    .replaceAll("website builder", "конструктора сайтов")
    .replaceAll("pixel perfect", "пиксельной точности")
    .replaceAll("production-ready front-end версткой", "готовой к продакшену фронтенд-вёрсткой")
    .replaceAll("OpenAI", "OpenAI")
    .replaceAll("Google’s", "Google");
}

let updated = 0;
for (const row of rows) {
  const mapped = descriptionMap.get(row.Slug);
  if (mapped) {
    row.Description = mapped;
    updated += 1;
    continue;
  }

  const normalized = genericNormalize(row.Description || "");
  if (normalized !== row.Description) {
    row.Description = normalized;
    updated += 1;
  }
}

await fs.writeFile(filePath, JSON.stringify(rows, null, 2), "utf8");
console.log(`updated:${updated}`);
