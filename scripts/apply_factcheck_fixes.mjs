for (const [oldStr, newStr] of fixes) {
  const n = s.split(oldStr).length - 1;
  if (n !== 1) {
    console.error(`✗ найдено ${n} раз (ожидалось 1): ${oldStr.slice(0, 60)}…`);
    failed = true;
  }
}
